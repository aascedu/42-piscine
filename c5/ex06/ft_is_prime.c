int	ft_is_prime(int nb)
{
	int	i;

	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	i = 2;
	while (i <= (nb / i) + 1)
	{
		if (nb % i == 0)
			return (0);
		i++;
	}
	return (1);
}
